#include <bits/stdc++.h>
using namespace std;
int32_t main()
{
    char c;
    cout<<"Enter an character: ";
    cin>>c;

    if(c=='a' || c=='e' || c=='i' || c=='o' || c=='u'){
        cout<<"Character is vowel"<<endl;
    }else if(c=='A' || c=='E' || c=='I' || c=='O' || c=='U'){
        cout<<"Character is vowel"<<endl;
    }else{
        cout<<"Character is not vowel"<<endl;
    }
    return 0;
}